package com.example.basickotlin.operator

fun main() {
    val pertambah :Int = 4
    val perkurang :Int = 7
    val perkalian :Int = 5
    val pembagian :Int = 2
    val sisa :Int = 3
    println(pertambah + perkurang)
    println(perkurang - pertambah)
    println(perkalian * pembagian)
    println(pembagian / perkalian)
    println(pertambah % sisa)
}